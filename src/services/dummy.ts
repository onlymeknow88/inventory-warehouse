// Accounts Receivable (Rekap Piutang) - Generated from Inquiries and Purchases
// Filter: URGENT dan BELUM TER-PO
export const accountsReceivables = [
  // From inquiries (Form Barang/Jasa) - convert to AccountsReceivable format
  ...inquiries
    .filter(inq => inq.items && inq.items.length > 0)
    .flatMap(inq => 
      inq.items!.map((item, idx) => ({
        id: inq.id * 1000 + idx, // Unique ID
        request_date: inq.inquiry_date,
        offer_letter_number: `INQ-${inq.id.toString().padStart(3, '0')}/${new Date(inq.inquiry_date).getFullYear()}`,
        delivery_date: inq.deadline || inq.inquiry_date,
        do_number: `DO-INQ-${inq.id.toString().padStart(3, '0')}`,
        delivery_proof_url: item.photo_url || '#',
        pending_days: Math.floor((new Date().getTime() - new Date(inq.inquiry_date).getTime()) / (1000 * 60 * 60 * 24)),
        user_name: getUserById(inq.user_id)?.name || 'Unknown',
        po_number: '', // Inquiry belum ada PO
        item_name: item.vendor_item_name,
        qty: item.qty,
        unit: item.unit,
        price_unit: item.price_unit,
        price_total: item.price_total,
        vendor_id: inq.vendor_id,
        status: 'pending' as 'pending' | 'paid' | 'overdue',
        created_at: inq.inquiry_date,
      }))
    ),
  // From purchases (Form Pembelian) - only URGENT and NO PO
  ...purchases
    .filter(p => p.is_urgent && !p.has_po)
    .map(purchase => ({
      id: purchase.id + 10000, // Offset to avoid ID collision
      request_date: purchase.created_at,
      offer_letter_number: `PROC-${purchase.id.toString().padStart(3, '0')}/${new Date(purchase.created_at).getFullYear()}`,
      delivery_date: purchase.delivery_deadline,
      do_number: `DO-${purchase.po_number}`,
      delivery_proof_url: '#',
      pending_days: Math.floor((new Date().getTime() - new Date(purchase.created_at).getTime()) / (1000 * 60 * 60 * 24)),
      user_name: getUserById(purchase.user_id)?.name || 'Unknown',
      po_number: purchase.po_number,
      item_name: purchase.job_detail,
      qty: purchase.qty,
      unit: purchase.unit,
      price_unit: purchase.price_unit,
      price_total: purchase.price_total,
      vendor_id: purchase.vendor_id,
      status: (Math.floor((new Date().getTime() - new Date(purchase.created_at).getTime()) / (1000 * 60 * 60 * 24)) > 180 
        ? 'overdue' 
        : 'pending') as 'pending' | 'paid' | 'overdue',
      created_at: purchase.created_at,
    })),
];

