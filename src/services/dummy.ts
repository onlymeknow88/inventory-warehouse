// Helper functions
export const getVendorById = (id: number) => vendors.find(v => v.id === id);
export const getItemById = (id: number) => items.find(i => i.id === id);
export const getUserById = (id: number) => users.find(u => u.id === id);
export const getPurchaseById = (id: number) => purchases.find(p => p.id === id);
export const getInquiryById = (id: number) => inquiries.find(i => i.id === id);
export const getTenderById = (id: number) => tenders.find(t => t.id === id);

export const getItemsByVendor = (vendorId: number) => {
  const relations = itemVendors.filter(iv => iv.vendor_id === vendorId);
  return relations.map(rel => ({
    ...getItemById(rel.item_id)!,
    vendor_item_name: rel.vendor_item_name,
    link_url: rel.link_url,
  }));
};

export const getVendorsByItem = (itemId: number) => {
  const relations = itemVendors.filter(iv => iv.item_id === itemId);
  return relations.map(rel => ({
    ...getVendorById(rel.vendor_id)!,
    vendor_item_name: rel.vendor_item_name,
    link_url: rel.link_url,
  }));
};

